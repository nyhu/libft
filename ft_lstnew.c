#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*new;
	void	*cpy;

	new = (t_list *)malloc(sizeof(t_list));
	cpy = (void *)malloc(content_size);
	if (!cpy || !new)
		return (NULL);
	new->content_size = content_size;
	new->content = ft_memcpy(cpy, content, content_size);
	new->next = NULL;
	return (new);
}
